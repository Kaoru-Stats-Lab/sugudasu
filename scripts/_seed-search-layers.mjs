function dedupeFromTo(rows) {
  const seen = new Set();
  const out = [];
  for (const r of rows) {
    if (!r.from || !r.to || r.from === r.to) continue;
    if (seen.has(r.from)) continue;
    seen.add(r.from);
    out.push({ from: r.from, to: r.to });
  }
  return out;
}

function mapToolIds(ids) {
  const out = [];
  const seen = new Set();
  for (const id of ids || []) {
    const mapped = Object.prototype.hasOwnProperty.call(TOOL_ID_MAP, id) ? TOOL_ID_MAP[id] : id;
    if (!mapped || seen.has(mapped)) continue;
    seen.add(mapped);
    out.push(mapped);
  }
  return out;
}

